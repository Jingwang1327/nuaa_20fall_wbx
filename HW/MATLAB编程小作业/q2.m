clc,clear,clf,close all;

X = -10:2:10;
Y = -10:2:10;
Z = X.^2-2*Y.^2;
[x,y] = meshgrid(-10:2:10);
[x1,y1] = meshgrid(-10:0.1:10);
[x2,y2] = meshgrid(-10:0.1:10);
[x3,y3] = meshgrid(-10:0.1:10);
xyz_limit = [-10 10 -10 10 -100 100];
z = x.^2-2*y.^2;
z1 = interp2(x,y,z,x1,y1,'nearest');
z2 = interp2(x,y,z,x2,y2,'linear');
z3 = interp2(x,y,z,x3,y3,'cubic');
figure(1);
subplot(2,2,1);
scatter3(x(:),y(:),z(:),10,'filled');
% plot3(x,y,z);
xlabel('X'),ylabel('Y'),zlabel('Z');
title('未经插值的散点图');
subplot(2,2,2);
plot3(x1,y1,z1);
xlabel('X'),ylabel('Y'),zlabel('Z');
title('nearest插值');
subplot(2,2,3);
plot3(x2,y2,z2);
xlabel('X'),ylabel('Y'),zlabel('Z');
title('linear插值');
subplot(2,2,4);
plot3(x3,y3,z3);
xlabel('X'),ylabel('Y'),zlabel('Z');
title('cubic插值');